clear; clc;

% 1. Подключение к RoboDK
RDK = Robolink;
robot = RDK.Item('UR5', RDK.ITEM_TYPE_ROBOT);

if ~robot.Valid()
    error('Робот UR5 не найден! Проверьте имя в дереве RoboDK.');
end

% 2. Считываем текущую (стандартную) позицию как стартовую
q_start = robot.Joints();
q_start = q_start(:)'; 
fprintf('Стартовая позиция зафиксирована.\n');

% 3. МАССИВ ИЗ 8 ТОЧЕК (Заполните сами)
% Впишите углы в формате: [q1, q2, q3, q4, q5, q6]
P = [
    0, -90, -90, 0, 90, 0; % Точка 1
    40.98, -94.36, -85.46, -0.18, 49.02, 0; % Точка 2
    40.98, -101.54, -108.53, 30.07, 49.02, 0; % Точка 3
    0.73, -97.35, -113.58, 30.93, 89.27, 0; % Точка 4
    2.04, -57.46, -143.20, 20.66, 87.96, 0; % Точка 5
    81.12, -72.94, -135.44, 28.38, 8.88, 0; % Точка 6
    81.12, -66.34, -109.17, -4.50, 8.88, 0; % Точка 7
    4.14, -53.96, -114.81, -11.23, 85.86, 0  % Точка 8
];

% 4. Настройки отрисовки пути
RDK.Command('TraceSize', '1'); % Тонкая линия
RDK.Command('Trace', 'On');    % Включить след
RDK.Command('Trace', 'Reset'); % Очистить старое

% Параметры движения
steps = 60; % Плавность (количество шагов между точками)

% 5. ЛОГИКА ДВИЖЕНИЯ
% Сначала двигаемся от стандартной позиции к первой точке
all_points = [q_start; P]; % Добавляем стартовую позицию в начало списка

disp('Робот начинает движение по точкам...');

for pt = 1:size(all_points, 1) - 1
    current_q = all_points(pt, :);
    target_q  = all_points(pt + 1, :);
    
    delta_q = target_q - current_q;
    
    fprintf('Переход к точке %d...\n', pt);
    
    % Интерполяция moveJ
    for i = 1:steps
        q_step = current_q + (delta_q * (i / steps));
        robot.setJoints(q_step);
        pause(0.01);
    end
end

% Возврат в стандартную позицию (опционально)
disp('Возврат в исходную позицию...');
q_last = P(end, :);
delta_back = q_start - q_last;
for i = 1:steps
    q_step = q_last + (delta_back * (i / steps));
    robot.setJoints(q_step);
    pause(0.01);
end

disp('Задание выполнено.');